typedef struct {
    int max_sfb;
    int window_sequence;
    int window_shape;
    int predictor;
    int num_window_groups;
    uint8_t grouping;
    uint8_t group_len[8];
} ics_struct;

typedef struct {
	int present;
	int n_filt[8];
	int length[8][4];
	int order[8][4];
	int direction[8][4];
	int coef[8][4][32];
} tns_struct;

typedef struct {
	int present;
	int mask[8][64];
} ms_struct;

typedef struct {
    AVCodecContext * avccontext;
    GetBitContext gb;
    VLC mainvlc;
    int sampling_index;
    int channels;
    VLC books[11];
    int * vq[11];
	const uint16_t *swb_offset_1024;
	const uint16_t *swb_offset_128;
    ics_struct ics[2];
	tns_struct tns[2];
    float coeffs[2][1024];
    float buf_mdct[2048];
    float saved[2][1024];
    float ret[1024]; // final output, also abused for mdct tmp
    int is_saved;
    MDCTContext mdct;
    MDCTContext mdct_small;
} aac_context_t;

